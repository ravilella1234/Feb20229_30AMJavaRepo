package kotak.loans.homeloans;

public class Multiplication 
{

	public static void main(String[] args) 
	{
		/*
		 * int n = 3;
		 * 
		 * for(int i=1;i<=10;i++) { System.out.println(n+"*"+i+"="+n*i); }
		 */
		
		for(int i=1;i<=10;i++)
		{
			for(int j=1;j<=10;j++)
			{
				System.out.print(i+"*"+j+"="+i*j + "  ");
			}
			System.out.println();
		}

	}

}
